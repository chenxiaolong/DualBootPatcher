package com.github.chenxiaolong.dualbootpatcher

import androidx.annotation.MainThread
import androidx.lifecycle.LifecycleOwner
import androidx.lifecycle.MutableLiveData
import androidx.lifecycle.Observer
import java.util.concurrent.atomic.AtomicBoolean

/**
 * A variant of [MutableLiveData] that will only fire off once.
 *
 * The observer will be notified only after [setValue] has been called beforehand. After an
 * observer receives a notification, it will not receive any more notifications until the next call
 * to [setValue].
 *
 * NOTE: Only one observer can be registered.
 */
class OneTimeLiveEvent<T> : MutableLiveData<T>() {
    private val pending = AtomicBoolean(false)

    @MainThread
    override fun observe(owner: LifecycleOwner, observer: Observer<in T>) {
        if (hasActiveObservers()) {
            throw IllegalStateException("Only one observer can be registered")
        }

        // Observe the internal MutableLiveData
        super.observe(owner, Observer { t ->
            if (pending.compareAndSet(true, false)) {
                observer.onChanged(t)
            }
        })
    }

    @MainThread
    override fun setValue(t: T?) {
        pending.set(true)
        super.setValue(t)
    }
}